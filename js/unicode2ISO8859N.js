	w.getISO8859NFromUnicode = function(codePoint) {

		var ascii, ret = [];
		// common area
		if (codePoint <= 0xa0) {
			ascii = codePoint.toString(16).toUpperCase().zeroPadding('even');
			return tables.map(function(e) {
				return {
					n: e.n,
					code: ascii
				};
			});
		}
		// else
		return tables.map(function(t) {
			var code = t.table[codePoint] || '';
			return {
				n: t.n,
				code: code.toString(16).toUpperCase()
			};
		}).filter(function(e) {
			return e.code.length;
		});
	};
